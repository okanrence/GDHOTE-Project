    [RoutePrefix(ConstantManager.ApiDefaultNamespace + "activity")]
    public class ActivityController : ApiController
    {
        [HttpGet]
        [Route("get-all-activities")]
        public HttpResponseMessage GetAllActivities(string startdate, string enddate)
        {
            try
            {
                var response = ActivityService.GetAllActivities(startdate, enddate).ToList();
                if (response.Count > 0)
                {
                    return new HttpResponseMessage(HttpStatusCode.OK)
                    {
                        RequestMessage = Request,
                        Content = new StringContent(
                            JsonConvert.SerializeObject(response, Formatting.Indented))
                    };
                }

                return new HttpResponseMessage(HttpStatusCode.NotFound)
                {
                    RequestMessage = Request,
                    Content = new StringContent(
                        JsonConvert.SerializeObject(response, Formatting.Indented))
                };
            }
            catch (UnableToCompleteException ex)
            {
                return Request.CreateResponse(HttpStatusCode.InternalServerError, ex.GetException());
            }
        }

        [HttpGet]
        [Route("get-member-activities")]
